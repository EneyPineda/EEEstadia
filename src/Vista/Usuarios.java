    private void mEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mEliminarMouseClicked
        if (txtId.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un Registro de la Tabla");
            return;
        }
        int i = JOptionPane.showConfirmDialog(this, "Si elimina el Producto lo borrara de las ventas asociadas a el 多Desea Eliminar?", "Confirmar Eliminacion", JOptionPane.YES_NO_OPTION);
        if (i == 0) {
            if (!txtId.getText().equals("")) {
                datUsuarios.setIdUsuario(txtId.getText());
                c.eliminar_usuario(datUsuarios);
                try {
                    cargar_tabla_usuarios("");
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpiar();
                bloquear();
            } else {

                JOptionPane.showMessageDialog(null, "No se elimino el producto.");
            }

        }
    }//GEN-LAST:event_mEliminarMouseClicked

    private void mGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mGuardarMouseClicked
        if (txtName.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un Nombre");
            txtName.requestFocus();
            return;
        }

        if (txtUser.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un Usuario");
            txtUser.requestFocus();
            return; 
        }

        if (txtPassword.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar una Contrase単a");
            txtPassword.requestFocus();
            return;
        }
        
        if (txtCURP.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar CURP");
            txtCURP.requestFocus();
            return;
        }

        if (txtRFC.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar RFC");
            txtRFC.requestFocus();
            return;
        }
        String idUsuario = txtId.getText();
        datUsuarios.setNombre(txtName.getText());
        datUsuarios.setUser(txtUser.getText());
        datUsuarios.setPassword(txtPassword.getText());
        datUsuarios.setFirmaD("Invalido");
        datUsuarios.setCURP(txtCURP.getText());
        datUsuarios.setRFC(txtRFC.getText());
        int acceso = cbAcceso.getSelectedIndex();
        datUsuarios.setAcceso((String)cbAcceso.getItemAt(acceso));
        
        if (c.Guardar_usuario(accion, datUsuarios, idUsuario)) {
            if (accion =="A") {
            JOptionPane.showMessageDialog(null, "Usuario Agregado.");
        } else if (accion =="M") {
            JOptionPane.showMessageDialog(null, "Usuario Actualizado.");
        }
        limpiar();
        bloquear();
            try {
                cargar_tabla_usuarios("");
            } catch (SQLException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_mGuardarMouseClicked

